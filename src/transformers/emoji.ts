/**
 * Transform emojis based on strategy
 */
export function transformEmojis(
  text: string, 
  strategy: 'remove' | 'name' | 'unicode' | 'preserve' = 'remove'
): string {
  if (strategy === 'preserve') {
    return text;
  }
  
  // Regex to match emojis
  const emojiRegex = /[\u{1F300}-\u{1F9FF}]|[\u{2600}-\u{26FF}]|[\u{2700}-\u{27BF}]|[\u{1F000}-\u{1F02F}]|[\u{1F0A0}-\u{1F0FF}]|[\u{1F100}-\u{1F1FF}]|[\u{1F200}-\u{1F2FF}]|[\u{1F680}-\u{1F6FF}]|[\u{1F900}-\u{1F9FF}]|[\u{1FA70}-\u{1FAFF}]|[\u{FE00}-\u{FE0F}]|[\u{1F018}-\u{1F270}]|[\u{238C}-\u{2454}]|[\u{20D0}-\u{20FF}]|[\u{FE00}-\u{FE0F}]/gu;
  
  return text.replace(emojiRegex, (match) => {
    if (strategy === 'remove') {
      return '';
    }
    
    if (strategy === 'unicode') {
      return match.codePointAt(0)?.toString(16) || '';
    }
    
    // strategy === 'name'
    const name = EMOJI_MAP.get(match);
    return name ? ` ${name} ` : '';
  });
}